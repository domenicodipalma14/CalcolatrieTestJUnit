package logic;

public class Calcolatrice {
	public double somma(double op1, double op2){
		return (op1+op2);
	}
	
	public double differenza(double op1, double op2){
		return (op1-op2);
	}
	
	public double divisione(double op1, double op2){
		return (op1/op2);
	}
	
	public double moltiplicazione(double op1, double op2){
		return (op1*op2);
	}
	
}
